import puppeteer from 'puppeteer';
import fs from 'fs/promises';

console.log('[Test] Starting simple PDF generation test...');

const browser = await puppeteer.launch({
  headless: true,
  executablePath: '/usr/bin/chromium',
  args: ['--no-sandbox', '--disable-setuid-sandbox'],
});

console.log('[Test] Browser launched');

const page = await browser.newPage();
console.log('[Test] Page created');

const html = `
  <!DOCTYPE html>
  <html>
    <head>
      <title>Solar Qualifier Test</title>
    </head>
    <body>
      <h1>Solar Qualifier Test</h1>
      <p>This is a test PDF generated by Puppeteer.</p>
      <p>Generated: ${new Date().toISOString()}</p>
    </body>
  </html>
`;

await page.setContent(html, { waitUntil: 'networkidle2' });
console.log('[Test] HTML content set');

const pdfBuffer = await page.pdf({
  format: 'A4',
  margin: { top: 20, bottom: 20, left: 20, right: 20 },
  printBackground: true,
});

console.log(`[Test] ✓ PDF generated: ${pdfBuffer.length} bytes`);

await fs.writeFile('/tmp/test.pdf', pdfBuffer);
console.log('[Test] ✓ PDF saved to /tmp/test.pdf');

await page.close();
await browser.close();
console.log('[Test] ✓ Browser closed');

process.exit(0);
